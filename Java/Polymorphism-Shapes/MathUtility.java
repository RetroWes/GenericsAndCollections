class MathUtility {
    // Method to add two integers
    static int sum(int a, int b) {
        return a + b;
    }
    // Method to add three integers
    static int sum(int a, int b, int c) {
        return a + b + c;
    }
    // Method to add two doubles
    static double sum(double a, double b) {
        return a + b;
    }
}